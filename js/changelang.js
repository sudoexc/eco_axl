// Функция для переключения языка
function changeLanguage(lang) {


    // Обновление элементов на основе перевода
    document.querySelectorAll('[data-translate-key]').forEach(element => {
        const key = element.getAttribute('data-translate-key');
       
        try {
            element.innerText = translations[lang][key] || element.innerText;
        } catch (error) {
            
        }

        try {
            element.placeholder = translations[lang][key] || element.placeholder;
        } catch (error) {

        }
    });
}

// Добавление обработчиков событий для кнопок
document.querySelectorAll('.dropdown-menu a').forEach(link => {
    link.addEventListener('click', (event) => {
        event.preventDefault();
        const lang = event.target.dataset.lang; // Получение выбранного языка
        changeLanguage(lang); // Вызов функции переключения языка
    });
});
